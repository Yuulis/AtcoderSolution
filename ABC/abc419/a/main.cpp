#include <bits/stdc++.h>
using namespace std;

// ======================================== //

int main()
{
    string S;
    cin >> S;

    if (S == "red")
        cout << "SSS" << endl;
    else if (S == "blue")
        cout << "FFF" << endl;
    else if (S == "green")
        cout << "MMM" << endl;
    else
        cout << "Unknown" << endl;

    return 0;
}